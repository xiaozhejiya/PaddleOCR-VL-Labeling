ALTER TABLE projects
    ALTER COLUMN created_by SET NOT NULL;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_projects_created_by'
    ) THEN
        ALTER TABLE projects
            ADD CONSTRAINT fk_projects_created_by
            FOREIGN KEY (created_by) REFERENCES users (id);
    END IF;
END;
$$;

