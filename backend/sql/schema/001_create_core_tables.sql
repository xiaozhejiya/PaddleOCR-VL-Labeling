-- Internal primary keys use BIGINT identity for index locality.
-- Internal foreign keys use <entity>_id.
-- Public stable ids use public_id. API route names may still use resource names
-- such as {page_id}, but database schema stores those values in public_id.

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION prevent_audit_logs_mutation()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    RAISE EXCEPTION 'audit_logs is append-only and cannot be %', TG_OP;
END;
$$;

